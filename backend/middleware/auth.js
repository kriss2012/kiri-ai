const jwt = require('jsonwebtoken');
const User = require('../models/User');

const protect = async (req, res, next) => {
  try {
    let token;

    if (req.headers.authorization && req.headers.authorization.startsWith('Bearer')) {
      token = req.headers.authorization.split(' ')[1];
    } else if (req.cookies && req.cookies.token) {
      token = req.cookies.token;
    }

    if (!token) {
      return res.status(401).json({
        success: false,
        message: 'Access denied. No token provided.'
      });
    }

    const decoded = jwt.verify(token, process.env.JWT_SECRET);
    const user = await User.findById(decoded.id);

    if (!user) {
      return res.status(401).json({
        success: false,
        message: 'User not found.'
      });
    }

    req.user = user;
    next();
  } catch (error) {
    return res.status(401).json({
      success: false,
      message: 'Invalid or expired token.'
    });
  }
};

// Check request limits
const checkRequestLimit = async (req, res, next) => {
  try {
    const user = req.user;

    if (!user.canMakeRequest()) {
      return res.status(429).json({
        success: false,
        message: 'Daily request limit reached. Upgrade to Premium for unlimited requests.',
        limitReached: true,
        dailyLimit: parseInt(process.env.FREE_DAILY_REQUESTS || 50),
        requestsUsed: user.dailyRequests
      });
    }

    next();
  } catch (error) {
    next(error);
  }
};

const generateToken = (id) => {
  return jwt.sign({ id }, process.env.JWT_SECRET, {
    expiresIn: process.env.JWT_EXPIRE || '7d'
  });
};

module.exports = { protect, checkRequestLimit, generateToken };
